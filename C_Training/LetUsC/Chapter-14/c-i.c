#include<stdio.h>
int main(){
  int n1[3][3],n2[3][3],i,j;
  puts("Enter first matrix elements");
  for(i=0;i<3;i++){
    for(j=0;j<3;j++){
      scanf("%d",&n1[i][j]);
    }
  }
  puts("\n Enter second matrix elements:");
  for(i=0;i<3;i++){
    for(j=0;j<3;j++){
      scanf("%d",&n2[i][j]);
    }
  }
  puts("\n Multiplied matrix");
  int sum=0,k;
  for(i=0;i<3;i++){ // row 0:2
    for(j=0;j<3;j++){ //col 0:2
      for(k=0;k<3;k++){ //()()()()+()()()()+()()()()
        sum=0;
        sum=sum+n1[i][k]*n2[k][j]; // ()()()()
        //sum=0;
      }
      printf("%d ",sum);
    }
    printf("\n");
  }
  return 0;
}
